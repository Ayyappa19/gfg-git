// User function template for C++
class Solution {
  public:
    int rowWithMax1s(vector<vector<int>> &arr) {
        // code here
        int maxi=0;
        for( int i=0;i<arr.size();i++){
            int k=0;
            for ( int j=1;j<arr[i].size();j++){
                arr[i][j]=arr[i][j]+arr[i][j-1];
                k=arr[i][j];
            }
            maxi=max(k,maxi);
            
        }
        if(maxi==0)return -1;
        for(int i=0;i<arr.size();i++){
            if(arr[i][arr[i].size()-1]==maxi)return i;
        }
        
        
    }
};

